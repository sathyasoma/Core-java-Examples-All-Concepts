package com.assignments;

public class SwitchTest {

	public static void main (String args [ ] ) 
	{ 
	 char ch = 'A'; 
	 switch (ch) 
	 { 
	 case 'A': 
	 System.out.println ("Value is A"); 
	 break; 
	 case 'B': 
	 System.out.println ("Value is B"); 
	 break; 
	 default: 
	 System.out.println ("Unknown Value"); 
	 } 
	 } 

}
